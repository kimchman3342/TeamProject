package object.day6;

public class Product {

    private String productName;
    private int price;
    private String company;

    // 커스텀 생성자 만들기
    public Product() {
        
    }
    

    // getter 만들기
    public String getProductName() {
        return productName;
    }

    public int getPrice() {
        return price;
    }

    public String getCompany() {
        return company;
    }
}
